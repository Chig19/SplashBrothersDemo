

num_1 = input("Input Number 1:   ")
num_2 = input("Input Number 2:   ")

plus = float(num_1) + float(num_2)
print('{0} plus {1} is {2}'.format(num_1, num_2, plus))

minus = float(num_1) - float(num_2)
print('{0} minus {1} is {2}'.format(num_1, num_2, minus))

times = float(num_1) * float(num_2)
print('{0} times {1} is {2}'.format(num_1, num_2, times))

divide = float(num_1) / float(num_2)
print('{0} divided by {1} is {2}'.format(num_1, num_2, divide))

remainder = float(num_1) % float(num_2)
print('{0} divided by {1} gives the remainder of {2}'.format(num_1, num_2, remainder))


